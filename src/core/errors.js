// 오류메시지에 개인정보 원문·문서 본문을 포함하지 않는다 (NFR-003, FR-706)

export const ERROR_CODES = {
  E001: '지원하지 않는 파일 형식입니다. TXT, CSV, XLSX, DOCX, HWPX 또는 텍스트 PDF 파일을 사용해 주십시오.',
  E002: '파일 크기가 처리 한도(20MB)를 초과했습니다.',
  E003: '파일을 읽을 수 없습니다. 파일이 사용 중이거나 접근이 제한되었을 수 있습니다.',
  E004: '암호 또는 DRM이 적용된 문서는 처리할 수 없습니다. 암호를 해제한 복사본을 사용해 주십시오.',
  E005: '손상된 문서입니다. 원본 프로그램에서 다시 저장한 후 시도해 주십시오.',
  E006: '문서를 분석할 수 없습니다. 지원하지 않는 문서 구조일 수 있습니다.',
  E007: '브라우저 메모리가 부족합니다. 다른 탭을 닫거나 더 작은 문서로 시도해 주십시오.',
  E008: '결과 파일을 생성하지 못했습니다. 다시 시도해 주십시오.',
  E009: '지원하지 않는 문서 내부 구조입니다.',
  E010: '사용자가 작업을 취소했습니다.',
};

export class AppError extends Error {
  constructor(code, detail = '') {
    // detail에는 개인정보가 아닌 일반 설명만 허용
    super(`[${code}] ${ERROR_CODES[code] || '알 수 없는 오류'}${detail ? ` (${detail})` : ''}`);
    this.code = code;
  }
}
